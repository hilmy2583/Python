# kelompok fungsi
import math

def tambah(bil1, bil2):
  hasil = bil1 + bil2
  print("Hasil dari penjumlahan", bil1, "+", bil2, "=", hasil)
def kurang(bil1, bil2):
  hasil = bil1 - bil2
  print("Hasil dari penjumlahan", bil1, "-", bil2, "=", hasil)
def kali(bil1, bil2):
  hasil = bil1 * bil2
  print("Hasil dari penjumlahan", bil1, "x", bil2, "=", hasil)
def bagi(bil1, bil2):
  hasil = bil1 / bil2
  print("Hasil dari penjumlahan", bil1, "+", bil2, "=", hasil)
def pangkat(bil1, bil2):
  hasil = math.pow(bil1, bil2)
  print("Hasil dari penjumlahan", bil1, "+", bil2, "=", hasil)
def modulus(bil1, bil2):
  hasil = bil1 % bil2
  print("Hasil dari penjumlahan", bil1, "+", bil2, "=", hasil)